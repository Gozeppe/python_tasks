# Задача 3. Повышение цен
# Дан список цен на пять товаров с точностью до копейки. Так как экономика даёт о себе знать, мы спрогнозировали, 
# что через год придётся повышать цены на X процентов, а ещё через один год — ещё на Y процентов.

# Напишите программу, которая получает на вход список цен на товары (вещественные числа, список генерируется также с помощью list comprehensions) 
# и выводит в одну строку общую сумму стоимости товаров за каждый год.

 

# Пример:

# Цена на товар: 1.09

# Цена на товар: 23.56

# Цена на товар: 57.84

# Цена на товар: 4.56

# Цена на товар: 6.78

# Повышение на первый год: 0

# Повышение на второй год: 10

# Сумма цен за каждый год: 93.83 93.83 103.21

prices = [float(input(f"Цена на товар {i + 1}: ")) for i in range(5)]
increases = [float(input(f"Повышение цены {j + 1}-й год: ")) for j in range(2)]

totals_per_year = []

for year, inc in enumerate(increases, start=1):
    updated_prices = [round(price * (1 + inc / 100), 2) for price in prices]
    total = round(sum(updated_prices), 2)
    totals_per_year.append(total)

print(f"Сумма цен за каждый год:", *totals_per_year)
