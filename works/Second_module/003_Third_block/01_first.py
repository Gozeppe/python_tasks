# и заодно к каждому удвоенному добавляет ещё один дополнительный.

# Пользователь вводит строку и дополнительный символ. Напишите программу, которая генерирует два списка: в первом списке каждый элемент 
# — удвоенная буква первой строки, во втором списке каждый элемент — конкатенация элемента первого списка и дополнительного символа.

 

# Пример:

# Введите строку: привет

# Введите дополнительный символ: !

 

# Список удвоенных символов: ['пп', 'рр', 'ии', 'вв', 'ее', 'тт']

